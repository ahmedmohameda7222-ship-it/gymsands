function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) return template;
  return Object.entries(values).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), template);
}

export function useNutritionTargetsTranslation() {
  const { language, dir } = useTranslation();
  const dictionary = language === "de" ? de : language === "ar" ? ar : en;
  const nt = useCallback((key: NutritionTargetsKey, values?: Record<string, string | number>) => interpolate(dictionary[key], values), [dictionary]);
  return { nt, language, dir, locale: language === "de" ? "de-DE" : language === "ar" ? "ar" : "en-US" };
}
