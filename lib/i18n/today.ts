const dictionaries = { en, de, ar } as const;

function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) return template;
  return Object.entries(values).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), template);
}

export function useTodayTranslation() {
  const { language, dir } = useTranslation();
  const dictionary = dictionaries[language];
  const tt = useCallback((key: TodayKey, values?: Record<string, string | number>) => interpolate(dictionary[key] ?? en[key], values), [dictionary]);
  return { language, dir, tt };
}

export type { TodayKey };
